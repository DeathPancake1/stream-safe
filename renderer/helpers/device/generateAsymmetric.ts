export default async function generateAsymmetricKeys() {
  //@ts-ignore
  const { publicKey, privateKey } = await window.electron.crypto.generateKeyPairSync('rsa', {
    modulusLength: 2048,
    publicKeyEncoding: {
      type: 'spki',
      format: 'pem',
    },
    privateKeyEncoding: {
      type: 'pkcs8',
      format: 'pem',
    },
  });
  return { publicKey, privateKey }
}